DROP TABLE IF EXISTS userLocation, userSettings, user;

CREATE TABLE user(
    userID VARCHAR(25),
    PRIMARY KEY(userID)
);

CREATE TABLE userSettings(
    userID      VARCHAR(25),
    play        BOOL,
    FOREIGN KEY(userID)
        REFERENCES user(userID)
        ON DELETE CASCADE
        ON UPDATE CASCADE
);

CREATE TABLE userLocation(
    userID      VARCHAR(25),
    longitude   DOUBLE(11, 8),
    latitude    DOUBLE(10, 8),
    timestamp   TIMESTAMP,
    FOREIGN KEY(userID)
        REFERENCES user(userID)
        ON DELETE CASCADE
        ON UPDATE CASCADE
);

